package gr.aueb.cf.ch2;

/**
 * Add two integers and display the sum in the standard output
 */

public class AddAPP {

    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 7;
        int sum = 0;

        sum = num1 + num2;

        System.out.printf("The sum of %d and %d is %d", num1, num2, sum);
    }
}
